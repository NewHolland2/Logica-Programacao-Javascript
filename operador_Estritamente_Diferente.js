console.log(5 !== 5);     // false (os valores são iguais)
console.log(5 !== '5');   // true (os valores são iguais, mas os tipos são diferentes)
console.log(5 !== 10);    // true (os valores são diferentes)
console.log(true !== 1);  // true (os valores são diferentes, embora ambos sejam considerados verdadeiros)

